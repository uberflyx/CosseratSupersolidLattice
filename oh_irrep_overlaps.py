#!/usr/bin/env python3
"""
oh_irrep_overlaps.py
====================
Computes the tunnelling amplitude for ALL 10 irreducible representations
of O_h by evaluating the angular overlap of each irrep's basis functions
with the FCC Peierls-Nabarro potential.

THE PHYSICS
===========
The PN potential on the FCC lattice is periodic. In Fourier space, it
has nonzero components at each reciprocal lattice vector G. The first
shell of G vectors for FCC is the {111} family (8 vectors), the second
is {200} (6 vectors).

A defect with angular symmetry Γ (an irrep of O_h) tunnels through
the PN barrier with an amplitude that depends on the OVERLAP between
the defect's angular pattern and the PN potential's angular structure.

The overlap integral is:
    I_Γ = Σ_G |V_G|² × |⟨Γ|Ŷ_G⟩|²

where the sum runs over reciprocal lattice vectors G, V_G is the
Fourier coefficient (Debye-Waller weighted), and ⟨Γ|Ŷ_G⟩ is the
projection of the G-vector direction onto the irrep basis functions.

This gives an effective barrier height for each channel, and hence
a tunnelling amplitude.

M. A. Cox, University of the Witwatersrand (2026)
"""

import numpy as np
from itertools import product

# ══════════════════════════════════════════════════════════════
# O_h CHARACTER TABLE AND BASIS FUNCTIONS
# ══════════════════════════════════════════════════════════════

# The 48 elements of O_h = O × {E, i}
# O has 24 elements: E, 8C₃, 3C₂, 6C₄, 6C₂'

# We work with the basis functions (cubic harmonics) for each irrep.
# These are polynomials in (x,y,z) that transform as each irrep.

# For a unit vector n̂ = (nx, ny, nz), the basis functions are:

def A1g(n):
    """Trivial rep: constant (= 1 everywhere)."""
    return np.array([1.0])

def A2g(n):
    """Pseudoscalar: xyz."""
    nx, ny, nz = n
    return np.array([nx * ny * nz])

def Eg(n):
    """Doublet: (2z²-x²-y², x²-y²)/√2."""
    nx, ny, nz = n
    return np.array([
        (2*nz**2 - nx**2 - ny**2) / np.sqrt(6),
        (nx**2 - ny**2) / np.sqrt(2)
    ])

def T1g(n):
    """Axial vector: (x(y²-z²), y(z²-x²), z(x²-y²))."""
    nx, ny, nz = n
    return np.array([
        nx * (ny**2 - nz**2),
        ny * (nz**2 - nx**2),
        nz * (nx**2 - ny**2)
    ])

def T2g(n):
    """Symmetric traceless off-diagonal: (yz, zx, xy)."""
    nx, ny, nz = n
    return np.array([ny*nz, nz*nx, nx*ny])

def A1u(n):
    """Parity-odd scalar: xyz(x²-y²)(y²-z²)(z²-x²)."""
    nx, ny, nz = n
    return np.array([nx*ny*nz * (nx**2-ny**2) * (ny**2-nz**2) * (nz**2-nx**2)])

def A2u(n):
    """Parity-odd pseudoscalar: xyz(x⁴(y²-z²)+cyclic)."""
    # Simplest A2u basis: xyz × (x⁴+y⁴+z⁴-3(x²y²+y²z²+z²x²)/2)
    # but for low-order G vectors, we can use the simpler form.
    # Actually A2u is generated by the product of A2g (xyz) and A1u,
    # but that's high order. For the {111} directions, A2u is
    # proportional to xyz × [sum of 4th order terms].
    # For simplicity: use x⁵yz - xy⁵z + xyz⁵ - ... (7th order)
    # Since we're evaluating on specific directions, the cleanest
    # approach is to use the character projection.
    nx, ny, nz = n
    # A2u transforms as the determinant times pseudoscalar:
    val = nx*ny*nz * (nx**4 + ny**4 + nz**4
                      - nx**2*ny**2 - ny**2*nz**2 - nz**2*nx**2)
    return np.array([val])

def T1u(n):
    """Polar vector: (x, y, z)."""
    nx, ny, nz = n
    return np.array([nx, ny, nz])

def T2u(n):
    """Rank-2 antisymmetric, odd parity: x(y²-z²), y(z²-x²), z(x²-y²)
    times the pseudoscalar structure... Actually T2u on unit vectors:
    T2u has basis functions x(y²+z²), y(z²+x²), z(x²+y²) in some
    conventions. Let me use the standard: """
    nx, ny, nz = n
    # T2u: xyz-weighted antisymmetric. Standard basis:
    # x(y⁴-z⁴), y(z⁴-x⁴), z(x⁴-y⁴)  [5th order]
    return np.array([
        nx * (ny**4 - nz**4),
        ny * (nz**4 - nx**4),
        nz * (nx**4 - ny**4)
    ])

def Eu(n):
    """Doublet, odd parity. Basis:
    (z(x²-y²)(x²+y²-2z²), (x²-y²)(2z²-x²-y²)×z) ... complicated.
    Use 5th order: """
    nx, ny, nz = n
    # Eu basis functions (5th order cubic harmonics):
    return np.array([
        nz * (2*nz**2 - nx**2 - ny**2) * nx * ny,
        (nx**2 - ny**2) * nz * (nx**2 + ny**2 - 2*nz**2) / np.sqrt(3)
    ])


irreps = {
    'A1g': (A1g, 1, 'even'),
    'A2g': (A2g, 1, 'even'),
    'Eg':  (Eg,  2, 'even'),
    'T1g': (T1g, 3, 'even'),
    'T2g': (T2g, 3, 'even'),
    'A1u': (A1u, 1, 'odd'),
    'A2u': (A2u, 1, 'odd'),
    'Eu':  (Eu,  2, 'odd'),
    'T1u': (T1u, 3, 'odd'),
    'T2u': (T2u, 3, 'odd'),
}


# ══════════════════════════════════════════════════════════════
# FCC RECIPROCAL LATTICE VECTORS
# ══════════════════════════════════════════════════════════════

# FCC reciprocal lattice = BCC in reciprocal space.
# First shell: {111} family — 8 vectors, |G| = π√3/a
# Second shell: {200} family — 6 vectors, |G| = 2π/a
# (using conventional FCC lattice constant a = ℓ√2)

# {111} directions (all permutations of ±1,±1,±1)
G111 = []
for s in product([-1, 1], repeat=3):
    G111.append(np.array(s, dtype=float))
G111 = np.array(G111)  # 8 vectors

# {200} directions (±1,0,0 and permutations)
G200 = []
for i in range(3):
    for s in [-1, 1]:
        v = np.zeros(3)
        v[i] = s
        G200.append(v)
G200 = np.array(G200)  # 6 vectors

# Normalise to unit vectors for angular overlap
G111_hat = G111 / np.linalg.norm(G111[0])
G200_hat = G200 / np.linalg.norm(G200[0])

# PN Fourier coefficients (Debye-Waller weighted)
# For the sinusoidal PN potential, V_G ∝ e^{-|G|w} where w is core width.
# w/ℓ = π/4 (from the α paper)
w_over_ell = np.pi / 4
ell = 1.0

# |G₁₁₁| = π√6/ℓ (for FCC with spacing ℓ)
G111_mag = np.pi * np.sqrt(6) / ell
# |G₂₀₀| = 2π√2/ℓ
G200_mag = 2 * np.pi * np.sqrt(2) / ell

V_G111 = np.exp(-G111_mag * w_over_ell)
V_G200 = np.exp(-G200_mag * w_over_ell)

print("=" * 72)
print("  ANGULAR OVERLAP INTEGRALS FOR ALL O_h IRREPS")
print("=" * 72)
print(f"""
  FCC reciprocal lattice shells:
    {{111}}: 8 vectors, |G| = π√6/ℓ = {G111_mag:.4f}/ℓ
    {{200}}: 6 vectors, |G| = 2π√2/ℓ = {G200_mag:.4f}/ℓ

  PN form factor (Lorentzian core, w/ℓ = π/4):
    V_{{111}} = e^{{-|G₁₁₁|w}} = {V_G111:.6e}
    V_{{200}} = e^{{-|G₂₀₀|w}} = {V_G200:.6e}

  The {{111}} shell dominates by a factor of {V_G111/V_G200:.0f}.
""")


# ══════════════════════════════════════════════════════════════
# COMPUTE ANGULAR OVERLAPS
#
# For each irrep Γ, compute:
#   I_Γ = Σ_G |V_G|² × ||f_Γ(Ĝ)||²
#
# where the sum runs over reciprocal lattice vectors,
# and ||f_Γ(Ĝ)||² is the squared norm of the basis function
# evaluated at the G direction.
#
# The overlap I_Γ determines the effective barrier height.
# Higher I_Γ = stronger coupling to the PN potential = more tunnelling.
# ══════════════════════════════════════════════════════════════

print(f"  Computing angular overlaps...")
print(f"\n  {'Irrep':<8} {'dim':>3} {'Parity':>6}  {'I_{{111}}':>12}  {'I_{{200}}':>12}  "
      f"{'I_total':>12}  {'I/I_T1u':>8}")
print(f"  {'─'*8} {'─'*3} {'─'*6}  {'─'*12}  {'─'*12}  {'─'*12}  {'─'*8}")

results = {}

for name, (func, dim, parity) in irreps.items():
    # Sum over {111} shell
    I_111 = 0.0
    for G_hat in G111_hat:
        f = func(G_hat)
        I_111 += np.sum(f**2)
    I_111 *= V_G111**2

    # Sum over {200} shell
    I_200 = 0.0
    for G_hat in G200_hat:
        f = func(G_hat)
        I_200 += np.sum(f**2)
    I_200 *= V_G200**2

    I_total = I_111 + I_200
    results[name] = (I_total, I_111, I_200, dim, parity)

# Normalise to T1u
I_T1u = results['T1u'][0]

for name in ['T2g', 'T1u', 'T1g', 'A1g', 'A2g', 'Eg', 'A1u', 'A2u', 'Eu', 'T2u']:
    I_total, I_111, I_200, dim, parity = results[name]
    ratio = I_total / I_T1u if I_T1u > 0 else 0
    print(f"  {name:<8} {dim:3d} {parity:>6}  {I_111:12.6e}  {I_200:12.6e}  "
          f"{I_total:12.6e}  {ratio:8.4f}")


# ══════════════════════════════════════════════════════════════
# TUNNELLING AMPLITUDES FROM THE OVERLAPS
# ══════════════════════════════════════════════════════════════

print(f"\n{'=' * 72}")
print(f"  TUNNELLING AMPLITUDES FOR ALL CHANNELS")
print(f"{'=' * 72}")

alpha = 1.0 / 137.035999177
alpha_inv = 1.0 / alpha

print(f"""
  The tunnelling amplitude in channel Γ is:

    λ_Γ ∝ e^{{-S_Γ}}  where  S_Γ = S₀ × (I_T1u / I_Γ)

  S₀ = π²/2 = {np.pi**2/2:.4f} is the bare PN action in the shear channel.
  The ratio I_T1u/I_Γ scales the effective barrier: a smaller overlap
  means a higher effective barrier and hence more suppression.

  For channels with I_Γ = 0, the tunnelling is exactly ZERO
  (the mode doesn't couple to the PN potential at all).
""")

S0 = np.pi**2 / 2  # bare PN action for T1u

print(f"  {'Irrep':<8} {'I/I_T1u':>8} {'S_eff':>8} {'|λ|':>14} {'|λ|/α':>12} {'Physical':>20}")
print(f"  {'─'*8} {'─'*8} {'─'*8} {'─'*14} {'─'*12} {'─'*20}")

for name in ['T2g', 'T1u', 'Eg', 'T1g', 'T2u', 'A2g', 'Eu', 'A1g', 'A1u', 'A2u']:
    I_total, I_111, I_200, dim, parity = results[name]

    if I_total < 1e-30:
        print(f"  {name:<8} {'0':>8} {'∞':>8} {'0 (exact)':>14} {'—':>12} {'FORBIDDEN':>20}")
        continue

    ratio = I_total / I_T1u
    if ratio > 0:
        # Scale the barrier: S_eff = S₀ / ratio  (smaller overlap → larger barrier)
        # Actually: the tunnelling T ∝ exp(-2πw_eff/ℓ) where w_eff scales inversely
        # with the coupling to the PN potential. Stronger coupling → smaller
        # effective width → more tunnelling.
        #
        # More precisely: the PN equilibrium gives w/ℓ ∝ 1/√(V_eff) where
        # V_eff ∝ I_Γ. So w_eff ∝ 1/√(I_Γ/I_T1u) × w₀.
        # Hence S_eff = 2πw_eff/ℓ = S₀/√(I_Γ/I_T1u).
        #
        # But for a simpler model: the barrier height scales as I_T1u/I_Γ,
        # and the tunnelling scales as exp(-S₀ × √(I_T1u/I_Γ)) for WKB.
        # Or, in the thin-barrier limit, λ_Γ = α^(I_T1u/I_Γ).
        #
        # The correct scaling depends on the barrier shape. For a sinusoidal
        # barrier: λ ∝ exp(-π√(V_eff/E)) ∝ exp(-S₀ × √(1/ratio))
        # For a square barrier: λ ∝ exp(-S₀/ratio)
        #
        # Let's use the WKB scaling (sinusoidal barrier):
        S_eff = S0 / np.sqrt(ratio)
        lam = np.exp(-S_eff)
        lam_over_alpha = lam / alpha

        # Physical identification
        if name == 'T2g':
            phys = "Strong (α_s)"
        elif name == 'T1u':
            phys = "EM (α)"
        elif name == 'T1g':
            phys = "Weak/chirality"
        elif name == 'A1g':
            phys = "Compression"
        elif name == 'Eg':
            phys = "Tensor (f₂?)"
        elif name == 'A2g':
            phys = "Pseudoscalar (η'?)"
        elif name == 'T2u':
            phys = "NEW PHYSICS?"
        elif name == 'Eu':
            phys = "NEW PHYSICS?"
        else:
            phys = ""

        print(f"  {name:<8} {ratio:8.4f} {S_eff:8.4f} {lam:14.6e} {lam_over_alpha:12.4e} {phys:>20}")
    else:
        print(f"  {name:<8} {'0':>8} {'∞':>8} {'0 (exact)':>14} {'—':>12} {'FORBIDDEN':>20}")


